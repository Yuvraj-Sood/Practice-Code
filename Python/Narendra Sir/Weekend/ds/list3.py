nums = [4,3,2,6,7,8,9,6,9]

print(nums)

#replace an element
nums[3] = 11
print(nums)

#replace a range
nums[0:2] = [3,7]
print(nums)

#add an element
nums.append(10)
print(nums)

nums.extend([20,30,40])
print(nums)

#insert an element
nums.insert(4,0)
print(nums)

del nums[0]
print(nums)



    
    