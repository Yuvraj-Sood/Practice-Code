class Pattern
{
void pattern(int n)
{
int i=0;
for(i=0;i<n;i++)
{
System.out.print("*");
}
System.out.println();
}
void pattern()
{
int i=0;
for(i=0;i<10;i++)
{
System.out.print("$");
}
System.out.println();
}
void pattern(int n,char c)
{
int i=0;
for(i=0;i<=n;i++)
{
System.out.print(c);
}
System.out.println();
}
}